
public class Main {
    public static void main(String[] args) {

        double PI = Math.PI;                          //generate pi
        double MULER = Math.E;                        //generate muler's number
        System.out.println(PI);
        System.out.println(MULER);

        double power = Math.pow(2, 5);                 //set the powers
        System.out.println(power);

        double absolute = Math.abs(-99);               //set the absolute value
        System.out.println(absolute);

        double squareRoot = Math.sqrt(9);              //find the square root
        System.out.println(squareRoot);

        double round = Math.round(3.24);              //round the number
        System.out.println(round);

        double roundUp = Math.ceil(3.14);            //round up a number
        System.out.println(roundUp);

        double roundDown = Math.floor(3.99);         //round down
        System.out.println(roundDown);

        int max = Math.max(3, 5);                    //find the max value
        System.out.println(max);

        int min = Math.min(5, 6);                    //find the low value
        System.out.println(min);

    }
}